Calculadora de Média:
Enunciado: Crie um programa que permita ao usuário inserir uma série de notas (números) e, em seguida, calcule e exiba a média das notas.

Verificador de Números Pares e Ímpares:
Enunciado: Desenvolva um programa que solicita ao usuário um número e verifica se ele é par ou ímpar. Exiba uma mensagem apropriada com o resultado.

Contador de Números Primos:
Enunciado: Crie um programa que solicita ao usuário um número inteiro positivo e conte quantos números primos existem entre 1 e o número inserido. Em seguida, exiba o resultado.

Calculadora de Fatorial:
Enunciado: Faça um programa que permita ao usuário inserir um número inteiro positivo e, em seguida, calcule e exiba o fatorial desse número. O fatorial de um número é o produto de todos os inteiros positivos menores ou iguais a ele.

Conversor de Temperatura:
Enunciado: Crie um programa que converta a temperatura de Celsius para Fahrenheit. Solicite ao usuário que insira uma temperatura em graus Celsius e exiba a temperatura equivalente em graus Fahrenheit.

Contagem Regressiva:
Enunciado: Escreva um programa que solicite ao usuário um número inteiro positivo e, em seguida, exiba uma contagem regressiva a partir desse número até zero.

Esses exercícios abordam diferentes aspectos do uso de variáveis, condicionais e loops em PHP e não envolvem arrays ou objetos. Eles são projetados para ajudá-lo a praticar suas habilidades de programação em PHP.